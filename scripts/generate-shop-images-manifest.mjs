#!/usr/bin/env node
/**
 * Scan public/shop-images/{prod_*}/ and write manifest for the shop API.
 */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import {
  listLocalImageFiles,
  publicImagePath,
  SHOP_IMAGES_PUBLIC_DIR,
} from "./lib/product-images.mjs";
import { readdir } from "node:fs/promises";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const MANIFEST_MJS = join(ROOT, "netlify/functions/lib/shop-images-manifest.mjs");
const MANIFEST_JSON = join(ROOT, SHOP_IMAGES_PUBLIC_DIR, "manifest.json");

async function listProductIds() {
  const base = join(ROOT, SHOP_IMAGES_PUBLIC_DIR);
  try {
    const entries = await readdir(base, { withFileTypes: true });
    return entries
      .filter((e) => e.isDirectory() && e.name.startsWith("prod_"))
      .map((e) => e.name)
      .sort();
  } catch {
    return [];
  }
}

export async function generateShopImagesManifest() {
  const productIds = await listProductIds();
  /** @type {Record<string, string[]>} */
  const manifest = {};

  for (const productId of productIds) {
    const files = await listLocalImageFiles(productId);
    if (files.length > 0) {
      manifest[productId] = files.map((filename) => publicImagePath(productId, filename));
    }
  }

  const json = `${JSON.stringify(manifest, null, 2)}\n`;
  const mjs =
    "/** Generated by generate-shop-images-manifest.mjs — do not edit */\n" +
    `export default ${json.trim()};\n`;

  await mkdir(dirname(MANIFEST_MJS), { recursive: true });
  await writeFile(MANIFEST_MJS, mjs, "utf8");
  await mkdir(dirname(MANIFEST_JSON), { recursive: true });
  await writeFile(MANIFEST_JSON, json, "utf8");

  return { productIds: productIds.length, withImages: Object.keys(manifest).length, manifest };
}

const isMain = process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1];

if (isMain) {
  const { withImages, manifest } = await generateShopImagesManifest();
  console.log(`✓ shop-images manifest: ${withImages} product(s) with local gallery`);
  for (const [id, urls] of Object.entries(manifest)) {
    console.log(`  ${id}: ${urls.length} image(s)`);
  }
}
